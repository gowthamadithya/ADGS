class Calculator:
    """
    This class provides basic arithmetic operations like addition and subtraction.
    """

    def add(self, a, b):
        """
        Adds two numbers.
        
        Args:
        a (int): First number
        b (int): Second number
        
        Returns:
        int: The sum of a and b
        """
        return a + b

    def subtract(self, a, b):
        """
        Subtracts one number from another.
        
        Args:
        a (int): First number
        b (int): Second number
        
        Returns:
        int: The difference of a and b
        """
        return a - b
